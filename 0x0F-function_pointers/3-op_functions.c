#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - adds a to b
 *
 * @a: 1st number
 * @b: 2nd number
 *
 * Return: result
 */

int op_add(int a, int b)
{
return (a + b);
}


/**
 * op_sub - subtracts a - b
 *
 * @a: 1st number
 * @b: 2nd number
 *
 * Return: result
 */

int op_sub(int a, int b)
{
return (a - b);
}


/**
 * op_mul - multiplies a * b
 *
 * @a: 1st number
 * @b: 2nd number
 *
 * Return: result
 */

int op_mul(int a, int b)
{
return (a * b);
}


/**
 * op_div - divides a / b
 *
 * @a: 1st number
 * @b: 2nd number
 *
 * Return: result
 */

int op_div(int a, int b)
{
return (a / b);
}


/**
 * op_mod - remainder of a / b
 *
 * @a: 1st number
 * @b: 2nd number
 *
 * Return: result
 */

int op_mod(int a, int b)
{
return (a % b);
}
